<template>
    <div class="page-hero" v-if="model">
        <div class="topbar bg-black py-2 px-3 d-flex al-center">
            <img src="../assets/images/logo.png" alt="logo" height="30" />
            <div class="px-2 flex-1 text-white">
                <span class>王者荣耀</span>
                <span class="ml-2">攻略站</span>
            </div>
            <div class="px-1">
                <router-link to="/" tag="div" class="text-white">更多英雄 &gt;</router-link>
            </div>
        </div>
        <div>{{model.name}}</div>
    </div>
</template>
<script>
export default {
    props: {
        id: { required: true }
    },
    data() {
        return {
            model: null
        };
    },
    methods: {
        async fetch() {
            const res = await this.$http.get(`heroes/${this.id}`);
            this.model = res.data;
        }
    },
    created() {
        this.fetch();
    }
};
</script>
